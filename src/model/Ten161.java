package model;

public class Ten161 {
    private int id;
    private String ho;
    private String ten;
    private String tenDem;

    public Ten161(String ho, String ten, String tenDem) {
        this.ho = ho;
        this.ten = ten;
        this.tenDem = tenDem;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getHo() {
        return ho;
    }

    public void setHo(String ho) {
        this.ho = ho;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getTenDem() {
        return tenDem;
    }

    public void setTenDem(String tenDem) {
        this.tenDem = tenDem;
    }
}
